package com.renchaigao.terms;

public class everdayTerms {
    private Integer id;
    private Integer index;
    private String addDay;
    private String termString;
    private String termInfo;
    private String termStyle;
    private Integer zanNum;
    private String state;

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Integer getZanNum() {
        return zanNum;
    }
    public void setZanNum(Integer zanNum) {
        this.zanNum = zanNum;
    }

    public String getTermStyle() {
        return termStyle;
    }

    public void setTermStyle(String termStyle) {
        this.termStyle = termStyle;
    }

    public String getTermInfo() {
        return termInfo;
    }

    public void setTermInfo(String termInfo) {
        this.termInfo = termInfo;
    }

    public String getTermString() {
        return termString;
    }

    public void setTermString(String termString) {
        this.termString = termString;
    }

    public String getAddDay() {
        return addDay;
    }

    public void setAddDay(String addDay) {
        this.addDay = addDay;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
